    private void comboFiguraItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboFiguraItemStateChanged
         int indice = comboFigura.getSelectedIndex();
        switch (indice) {
            case 0:
                label1.setText("Lado"); 
                label2.setText(""); 
                label1.setVisible(true);
                label2.setVisible(false);
                
                text1.setText("");
                text2.setText("");
                text1.setVisible(true);
                text2.setVisible(false);
                break;
            case 1:
                label1.setText("Lado 1"); 
                label2.setText("Lado 2"); 
                label1.setVisible(true);
                label2.setVisible(true);
                
                text1.setText("");
                text2.setText("");
                text1.setVisible(true);
                text2.setVisible(true);
                break;
            case 2:
                label1.setText("Perimetro"); 
                label2.setText(""); 
                label1.setVisible(true);
                label2.setVisible(false);
                
                text1.setText("");
                text2.setText("");
                text1.setVisible(true);
                text2.setVisible(false);
                break;
            case 3:
                label1.setText("Base"); 
                label2.setText("Altura"); 
                label1.setVisible(true);
                label2.setVisible(true);
                
                text1.setText("");
                text2.setText("");
                text1.setVisible(true);
                text2.setVisible(true);
                break;
        }
    }//GEN-LAST:event_comboFiguraItemStateChanged

