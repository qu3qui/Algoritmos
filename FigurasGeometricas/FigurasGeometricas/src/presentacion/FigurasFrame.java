    private void areaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_areaButtonActionPerformed

        int parametro1 = Utilidades.obtenerParametro(text1.getText());
        int parametro2 = Utilidades.obtenerParametro(text2.getText());

        switch (comboFigura.getSelectedIndex()) {
            case 0:
                Cuadrado cuadrado = new Cuadrado(parametro1);
                respuesta.setText(Integer.toString(cuadrado.area()));
                break;
            case 1:
                Rectangulo rectangulo = new Rectangulo(parametro1, parametro2);
                respuesta.setText(Integer.toString(rectangulo.area()));
                break;
            case 2:
                Circulo circulo = new Circulo(parametro1);
                respuesta.setText(Integer.toString(circulo.area()));
                break;
            case 3:
                Triangulo triangulo = new Triangulo(parametro1, parametro2);
                respuesta.setText(Integer.toString(triangulo.area()));
                break;
        }
    }//GEN-LAST:event_areaButtonActionPerformed

